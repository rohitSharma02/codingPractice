package leetcode;

public class MergeSort {
    public static void main(String[] args) {
       int x = 5;
       int rem;
       boolean flag = false;

       for(int i = 0; i<= x/2; i++){
           rem = x%i;
           if(rem == 0)
               flag = false;
               break;
       }
       if(flag = true)
           System.out.println();
       else
           System.out.println();
    }
}
